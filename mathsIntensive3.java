public class mathsIntensive3 {
    public static void main(String[]args){

        // Java maths classes :,(
        // Math.max(x,y) and Math.min(x,y) used to find...
        //... highest/lowest value of x and y

        System.out.println(Math.max(10,15));
        System.out.println(Math.min(15,17));

        // sqrt = square root not an innuendo

        System.out.println(Math.sqrt(81));

        //abs = positive value of x

        System.out.println(Math.abs(-5.9));

        // random - pretty self explanatory >:D

        System.out.println(Math.random());

        // specify range using formula:

        int randomNum = (int)(Math.random() * 101);

        System.out.println(randomNum);
        

    }
    
}
